    private void SignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignInActionPerformed
        if (this.Email.getText().length() == 0 || String.valueOf(this.Password.getParent()).length() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập Email hoặc password", "Thông báo", 1);
        } else {
            if(Controllers.UserController.LogIn(this.Email.getText(), String.valueOf(this.Password.getPassword()))) {
                this.dispose();
                new Main().setVisible(true);
            }
        }
    }//GEN-LAST:event_SignInActionPerformed

    private void showPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPasswordActionPerformed
        if (showPassword.isSelected()) {
            Password.setEchoChar((char) 0);
        } else {
            Password.setEchoChar('*');
        }
    }//GEN-LAST:event_showPasswordActionPerformed

