    private void PasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PasswordKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (this.Email.getText().length() == 0 || String.valueOf(this.Password.getParent()).length() == 0) {
                JOptionPane.showMessageDialog(null, "Bạn chưa nhập Email hoặc password", "Thông báo", 1);
            } else {
                if(Controllers.UserController.LogIn(this.Email.getText(), String.valueOf(this.Password.getPassword()))) {
                    this.dispose();
                    new Main().setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_PasswordKeyPressed

